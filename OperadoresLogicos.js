// Operadores LÃ³gicos
let a = true;
let b = false;
console.log(a);
console.log(b);

// Operador logico && (and o Y)
// Regresa verdadero si ambos operandos
// son verdaderos 
console.log(`${a} && ${b} -> ${a && b}`);

// Operador logico || (or o O)
// Resumen verdadero si cualquiera de los valores es verdadero
console.log(`${a} || ${b} -> ${a || b}`);

// Operador ! (not o NO)
// Invierte el valor original, true -> false, false -> true
console.log(`${a} -> ${!a}`);
